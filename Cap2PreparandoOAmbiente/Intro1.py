# ! Conceito de váriaveis e atribuição
# * Variáveis -> Armazenam dados e dão nome a uma área da memória do comuputador (= -> Recebe)
# ? Sintaxe: nome_da_variavel = dado (nome_da_variavel recebe dado)
a = 2 # a recebe 2
b = 3 # b recebe 3
print(a + b)

